<h3><strong>What Information Do We Collect</strong></h3>
Upon registration for the system, the following information will be collected:
<ul class="ml-5" style="list-style-type: disc; padding-left: 1rem;">
    <li><strong>Full Name</strong> – for identification and ensuring accountability when using the system</li>
    <li><strong>Contact Information</strong> – Phone number and email must be provided for direct communication and support.</li>
    <li><strong>Area of Assignment</strong> – to properly assign the user's role and responsibilities within the system</li>
</ul>
<br>
This system will also collect the following information from beneficiaries:
<ul class="ml-5" style="list-style-type: disc; padding-left: 1rem;">
    <li><strong>Basic Identifying Information:</strong> Name, date of birth, and any relevant information.</li>
    <li><strong>Nutritional Status Data:</strong> Weight, height, and other relevant measurements as determined by the encoder.</li>
    </li><strong>Name of Parent/Guardian:</strong> As provided by the parent or guardian.</li>
</ul>
<br>
<h3><strong>How Do We Collect the Information</strong></h3>
During the registration process, users will personally enter their information into the system. Sensitive personal information will be gathered directly from beneficiaries or their parents/guardians, and only authorized individuals are permitted to enter the information into the system.
<br>
<br>
<h3><strong>How We Use Your Information</strong></h3>
The system collects and processes sensitive personal data of beneficiaries who are children aged 3 to 5 years. The collection tool was developed in accordance with Administrative Order No. 004, Series of 2011: Guidelines in the Implementation of the Supplemental Feeding Program and Administrative Order No. 008, Series of 2012: Omnibus Guidelines in the Implementation of the Supplementary Feeding Program. The information collected is used for the following purposes:
<ul class="ml-5" style="list-style-type: disc; padding-left: 1rem;">
    <li><strong>Identification:</strong> The names of the registered users in the system will be utilized for authorization purposes.</li>
    <li><strong>Assessing and Monitoring Nutritional Status:</strong> To evaluate the nutritional status of children and identify those who may require intervention or support.</li>
    <li><strong>Program Planning and Evaluation:</strong> To plan, implement, and evaluate programs and services aimed at improving child nutrition.</li>
    <li><strong>Statistical Analysis:</strong> To generate statistical reports and analyze trends in child nutrition for research and public health purposes.</li>
    <li><strong>Reporting to Relevant Authorities and Organizations:</strong> To comply with reporting requirements and provide data to relevant authorities for monitoring and policy development.</li>
</ul>
<br>
<h3><strong>Who Has Access to Your Information</strong></h3>
The personal data collected through the DSWD FO XI SFP ONSE System will only be shared and disclosed to authorized internal and external personnel for service processing. These include:
<ul class="ml-5" style="list-style-type: disc; padding-left: 1rem;">
    <li>DSWD Central Office: to conduct assessment and monitor consolidated reports to enhance the effectiveness of service delivery</li>
    <li>SFP Personnel (Nutritionist): to manage and coordinate/supervise the regional implementation of the program</li>
    <li>Local Government Units: to coordinate, monitor, and provide augmentation to the implementation of the program</li>
</ul>
<br>
<h3><strong>How Long Do We Keep Your Information</strong></h3>
We will retain the beneficiary’s personal information only for as long as necessary to achieve the purposes for which it was collected, or as required by law.
<br>
<h3><strong>Rights of Parents/Guardians</strong></h3>
Parents or guardians of beneficiaries possess several fundamental rights pertaining to the management and safeguarding of their children's personal information. Under the Data Privacy Act of 2012, they have the right to:
<ul class="ml-5" style="list-style-type: disc; padding-left: 1rem;">
    <li><strong>Right to Access:</strong> To request access to their child's personal information.</li>
    <li><strong>Right to Correct:</strong> To request that inaccurate or incomplete information be corrected.</li>
    <li><strong>Right to Delete:</strong> To request the deletion of their child's personal information, subject to legal limitations.</li>
    <li><strong>Right to Object:</strong> To object to the processing of their child's personal information, subject to legal limitations.</li>
    <li><strong>Right to Withdraw Consent:</strong> To withdraw their consent to the processing of their child's personal information, where consent is the basis for processing.</li>
</ul>
